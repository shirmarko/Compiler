apply:
    push rbp
    mov r13, rbp
    mov rbp, rsp

    mov rdx, MAGIC
    push rdx

    mov r10, 0              ;counter=0
    ;count
    mov r15, PARAM_COUNT
    sub r15, 1              ;because of the change to num args + magic
    add r15, 3
    shl r15, 3
    add r15, rbp            ; r15 = rbp+(3+PARAM_COUNT)*WORD_SIZE
    mov rdx, qword [r15]      ;rdx=the place of the last param= the list
    mov r12, rdx
    mov rdx, [rdx]
    mov r11, r12            ;r11 saves the list pointer
    mov sil, byte [r12]
    cmp sil, T_NIL          ;check the list is empty
    je .End_count

    .Count_Loop:
    mov sil, byte [r12]
    cmp sil, T_PAIR
    jne .End_count
    inc r10                     ;not pair, inc counter by 1
    CDR rsi, r12                ;returns the cdr (pointer)  
    ;mov rdx, [rsi]
    mov r12, rsi
    jmp .Count_Loop
;mov %1, qword [%2+TYPE_SIZE]	
    .End_count:

    cmp r10, 0
    je .End_pushing_list_loop
    
    
    mov rdx, r10
    shl rdx, 3                  ; rdx = r10*8
    MALLOC rax, rdx             ;rax holds the malloc pointer

    mov rcx, 0  
    .Copy_loop:
    cmp rcx, r10                ; list size
    je .End_copy_loop
    CAR rbx, r11                ;r11 saves the list 
    mov [rax], rbx              
    add rax, 8
    inc rcx
    CDR rdx, r11
    mov r11, rdx
    jmp .Copy_loop
    .End_copy_loop:

    mov rcx, 0
    .Pushing_list_loop:
    cmp rcx, r10
    je .End_pushing_list_loop
    sub rax, 8
    mov rbx, [rax]
    push rbx
    inc rcx
    jmp .Pushing_list_loop
    .End_pushing_list_loop:

    ;push args, n-args, env, ret
    mov r15, PARAM_COUNT
    sub r15, 2                      ; number of args without proc and list
    sub r15, 1                      ;the change of num args + magic
    mov rcx,0
    mov rsi, rbp                ; 40-8 = 32 -> 40 for the first arg, +r15 - to go to the last arg != list
    add rsi, 32
    mov rdi, r15
    shl rdi, 3
    add rsi, rdi                ;rsi= r15*8
    .Pushing_args_loop:
    cmp rcx, r15
    je .End_args_loop
    mov rdi, [rsi]                  ;the first arg to push
    push rdi
    inc rcx
    sub rsi, 8  
    jmp .Pushing_args_loop
    
    .End_args_loop:
    add r10, r15                    ;new args number
    add r10, 1                      ;adds "magic"
    push r10
    mov rsi , qword [rbp+32]        ;pointer of the proc from stack
    ;mov rsi, [rsi]      
    CLOSURE_ENV rax, rsi
    push rax                        ;push closure env
    CLOSURE_CODE rax, rsi
    mov rbx, qword [rbp + 8]
    push rbx                        ; push ret address
    add r10, 3                      ; new arg count + 3(env, ret, n-args)
    SHIFT_FRAME_REG r10
    
    CLOSURE_CODE rax, rsi
    mov rbp, r13
    jmp rax                         ;jmp to proc CODE                



